public class Motor
{
	private String Brand;
	private String Type;
	private String Engine;
	private String FrontTyre;
	private String RearTyre;
	private String Cooling;
	private String Frame;
	private double Capacity;
	private double Power;
	private double Torque;
	private int Year;
	private int Topspeed;
	private int FuelCapacity;
	private int Price;
	
	
	public void setBrand(String Brand)
	{
		this.Brand = Brand;
	}	
	public String getBrand()
	{
		return this.Brand;
	}
	
	public void setType(String Type)
	{
		this.Type = Type;
	}	
	public String getType()
	{
		return this.Type;
	}
	
	public void setEngine(String Engine)
	{
		this.Engine = Engine;
	}	
	public String getEngine()
	{
		return this.Engine;
	}

	public void setFrontTyre(String FrontTyre)
	{
		this.FrontTyre = FrontTyre;
	}	
	public String getFrontTyre()
	{
		return this.FrontTyre;
	}
	
	public void setRearTyre(String RearTyre)
	{
		this.RearTyre = RearTyre;
	}	
	public String getRearTyre()
	{
		return this.RearTyre;
	}

	public void setCooling(String Cooling)
	{
		this.Cooling = Cooling;
	}	
	public String getCooling()
	{
		return this.Cooling;
	}

	public void setFrame(String Frame)
	{
		this.Frame = Frame;
	}	
	public String getFrame()
	{
		return this.Frame;
	}	
	
	public void setPower(double Power)
	{
		this.Power = Power;
	}	
	public double getPower()
	{
		return this.Power;
	}

	public void setTorque(double Torque)
	{
		this.Torque = Torque;
	}	
	public double getTorque()
	{
		return this.Torque;
	}
	
	public void setCapacity(double Capacity)
	{
		this.Capacity = Capacity;
	}	
	public double getCapacity()
	{
		return this.Capacity;
	}

	public void setTopspeed(int Topspeed)
	{
		this.Topspeed = Topspeed;
	}	
	public int getTopspeed()
	{
		return this.Topspeed;
	}
	
	public void setFuelCapacity(int FuelCapacity)
	{
		this.FuelCapacity = FuelCapacity;
	}	
	public int getFuelCapacity()
	{
		return this.FuelCapacity;
	}
	
	public void setPrice(int Price)
	{
		this.Price = Price;
	}	
	public int getPrice()
	{
		return this.Price;
	}
	
	public void setYear(int Year)
	{
		this.Year = Year;
	}	
	public int getYear()
	{
		return this.Year;
	}	
}